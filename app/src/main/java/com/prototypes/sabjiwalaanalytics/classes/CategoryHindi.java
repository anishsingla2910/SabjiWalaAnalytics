package com.prototypes.sabjiwalaanalytics.classes;

public class CategoryHindi {
    public String category;
    public String category_hindi;
    
    public CategoryHindi(String category, String category_hindi) {
        this.category = category;
        this.category_hindi = category_hindi;
    }
    
    public String getCategory() {
        return category;
    }
    
    public void setCategory(String category) {
        this.category = category;
    }
    
    public String getCategory_hindi() {
        return category_hindi;
    }
    
    public void setCategory_hindi(String category_hindi) {
        this.category_hindi = category_hindi;
    }
}
